# Função para extrair informações da div
def extrair_info_div(soup):
    meta_div = soup.find('div', id='meta')
    if not meta_div:
        return {}

    paragrafos = meta_div.find_all('p')
    dados_paragrafos = {f'Parágrafo {i+1}': p.get_text(strip=True) for i, p in enumerate(paragrafos)}

    imagens = meta_div.find_all('img')
    urls_imagens = [img['src'] for img in imagens if 'src' in img.attrs] if imagens else None

    dados_div = {**dados_paragrafos, 'URLs de Imagens': urls_imagens}

    return dados_div

# Função para extrair dados das tabelas
def extrair_dados_tabelas(soup):
    tabelas = soup.find_all('table')
    dados_tabelas = []
    for tabela in tabelas:
        # Extrair cabeçalhos de duas linhas
        cabecalhos = []
        thead = tabela.find('thead')
        if thead:
            for tr in thead.find_all('tr'):
                cabecalho_nivel = [th.get_text(strip=True) for th in tr.find_all('th')]
                cabecalhos.append(cabecalho_nivel)

        # Extrair dados das linhas
        tbody = tabela.find('tbody')
        if tbody:
            linhas = tbody.find_all('tr')
            for linha in linhas:
                celulas = linha.find_all('td')
                dados_linha = [celula.text for celula in celulas]

                # Adicionar data da primeira coluna
                data_celula = linha.find('th', scope='row')
                if data_celula:
                    link_data = data_celula.find('a')
                    if link_data:
                        dados_linha.insert(0, link_data.text)  # Adiciona a data no início da linha de dados

                dados_tabelas.append(dados_linha)

        # Adicionando cabeçalhos e dados ao dicionário
        dados_totais = {
            'Cabeçalhos': cabecalhos,
            'Dados': dados_tabelas
        }
    return dados_totais

